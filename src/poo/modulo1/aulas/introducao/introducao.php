/**
 * PROGRAMAÇÃO ORIENTADA A OBJETOS
 * 
 * PROGRAMAÇÃO LINEAR: Necessário repetir o código para que consiga executar um novo algoritmo
 * PROGRAMAÇÃO ESTRUTURADA: Várias funções que consomem da mesma base de dados, filtrando e depois utilizando os que interessam
 * PROGRAMAÇÃO MODULAR: Cada módulo contém um conjunto de funções que serão usadas pra determinada tarefa, mas ainda consomem da mesma base de dados
 * PROGRAMAÇÃO ORIENTADA A OBJETOS: Cada objeto já contém seus próprios dados e funções, a principal característica é a comunicação entre objetos. Pensando em programação modular é como se cada módulo se comunicasse entre si, mas cada módulo já contendo os próprios dados e funções
 * 
 * HERANÇA: herda os dados do objeto pai
 * OBS: Não só os dados, mas o filho acaba se tornando um subtipo do pai, onde o pai for aceito o filho também será
 * POLIMORFISMO: objetos redefinem comportamentos herdados
 * OBS 2: Você pode ter várias versões diferentes da mesma função. Ou seja, implementações diferentes escolhidas de acordo com o tipo de polimorfismo
 * ABSTRAÇÃO: foca apenas no essencial para o uso do objeto
